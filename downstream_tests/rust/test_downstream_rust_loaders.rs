/// Check one windowed lengths downstream fixture.
fn check_windowed_lengths(env_var: &str, default_name: &str, has_blacklist: bool) -> Result<()> {
    let lengths = load_lengths_output(fixture_dir_path(env_var, default_name, FixtureKind::File)?)?;

    assert_eq!(lengths.row_mode(), LengthOutputMode::Windows);
    assert_f64_slice_near(lengths.counts_row_major(), EXPECTED_LENGTH_COUNTS_WINDOWED);
    let windows = lengths.window_metadata()?;
    assert_eq!(
        windows
            .iter()
            .map(|window| (
                window.index,
                window.chrom.as_str(),
                window.interval.as_tuple()
            ))
            .collect::<Vec<_>>(),
        vec![
            (0, "chr1", (0, 100)),
            (1, "chr1", (100, 200)),
            (2, "chr1", (200, 300)),
            (3, "chr1", (300, 360)),
        ]
    );
    let expected_blacklist = has_blacklist.then_some([0.04, 0.05, 0.1, 0.25]);
    assert_optional_f64_values(
        windows
            .iter()
            .map(|window| window.blacklisted_fraction)
            .collect::<Vec<_>>()
            .as_slice(),
        expected_blacklist.as_ref().map(|values| values.as_slice()),
    );
    assert_f64_slice_near(
        lengths.select().windows(&[3]).read()?.counts_row_major(),
        &[1.0, 0.0, 0.0],
    );

    let selected = lengths
        .select()
        .windows(&[1, 3])
        .length_range(Interval::new(50, 100)?)
        .read()?;
    assert_eq!(selected.row_indices(), &[1, 3]);
    assert_eq!(selected.shape(), (2, 2));
    assert_eq!(
        selected
            .window_metadata()?
            .iter()
            .map(|window| (
                window.index,
                window.chrom.as_str(),
                window.interval.as_tuple()
            ))
            .collect::<Vec<_>>(),
        vec![(1, "chr1", (100, 200)), (3, "chr1", (300, 360))]
    );
    assert_f64_slice_near(selected.counts_row_major(), &[2.0, 0.0, 0.0, 0.0]);

    Ok(())
}

/// Check one grouped lengths downstream fixture.
fn check_grouped_lengths(env_var: &str, default_name: &str, has_blacklist: bool) -> Result<()> {
    let lengths = load_lengths_output(fixture_dir_path(env_var, default_name, FixtureKind::File)?)?;

    assert_eq!(lengths.row_mode(), LengthOutputMode::Groups);
    assert_eq!(lengths.group_index("gamma")?, 2);
    assert_f64_slice_near(lengths.counts_row_major(), EXPECTED_LENGTH_COUNTS_GROUPED);
    let groups = lengths.group_metadata()?;
    assert_eq!(
        groups
            .iter()
            .map(|group| (group.index, group.name.as_str(), group.eligible_windows))
            .collect::<Vec<_>>(),
        vec![
            (0, "beta", 2),
            (1, "alpha", 1),
            (2, "gamma", 1),
            (3, "zero", 1)
        ]
    );
    let expected_blacklist = has_blacklist.then_some([0.07, 0.05, 0.25, 0.333]);
    assert_optional_f64_values(
        groups
            .iter()
            .map(|group| group.blacklisted_fraction)
            .collect::<Vec<_>>()
            .as_slice(),
        expected_blacklist.as_ref().map(|values| values.as_slice()),
    );
    assert_f64_slice_near(
        lengths
            .select()
            .groups_by_name(&["beta"])
            .read()?
            .counts_row_major(),
        &[2.0, 0.0, 1.0],
    );

    let selected = lengths
        .select()
        .groups_by_name(&["beta", "zero"])
        .length_range(Interval::new(50, 100)?)
        .read()?;
    assert_eq!(selected.row_indices(), &[0, 3]);
    assert_eq!(selected.shape(), (2, 2));
    assert_f64_slice_near(selected.counts_row_major(), &[0.0, 1.0, 0.0, 0.0]);
    assert_eq!(
        selected
            .group_metadata()?
            .iter()
            .zip(selected.counts().rows())
            .map(|(group, length_counts)| {
                (
                    group.name.as_str(),
                    length_counts.iter().copied().sum::<f64>(),
                )
            })
            .collect::<Vec<_>>(),
        vec![("beta", 1.0), ("zero", 0.0)]
    );

    Ok(())
}

#[derive(Debug, Clone, Copy)]
enum FixtureKind {
    Directory,
    File,
}

/// Resolve and validate a downstream fixture path.
fn fixture_dir_path(env_var: &str, default_name: &str, kind: FixtureKind) -> Result<PathBuf> {
    let path = env::var_os(env_var)
        .map(PathBuf::from)
        .unwrap_or_else(|| Path::new(FIXTURE_DIR).join(default_name));
    match kind {
        FixtureKind::Directory => ensure!(
            path.is_dir(),
            "{env_var} fixture directory does not exist at {}. Generate downstream fixtures first.",
            path.display()
        ),
        FixtureKind::File => ensure!(
            path.is_file(),
            "{env_var} fixture file does not exist at {}. Generate downstream fixtures first.",
            path.display()
        ),
    }
    Ok(path)
}

/// Assert two `f32` slices are equal within test tolerance.
fn assert_f32_slice_near(actual: &[f32], expected: &[f32]) {
    assert_eq!(actual.len(), expected.len(), "slice lengths differ");
    for (index, (&actual_value, &expected_value)) in actual.iter().zip(expected).enumerate() {
        assert!(
            (actual_value - expected_value).abs() <= 1e-6,
            "value {index} differs: actual {actual_value}, expected {expected_value}"
        );
    }
}

/// Assert two `f64` slices are equal within test tolerance.
fn assert_f64_slice_near(actual: &[f64], expected: &[f64]) {
    assert_eq!(actual.len(), expected.len(), "slice lengths differ");
    for (index, (&actual_value, &expected_value)) in actual.iter().zip(expected).enumerate() {
        assert!(
            (actual_value - expected_value).abs() <= 1e-9,
            "value {index} differs: actual {actual_value}, expected {expected_value}"
        );
    }
}

/// Assert optional blacklist fractions match expected presence and values.
fn assert_optional_f64_values(actual: &[Option<f64>], expected: Option<&[f64]>) {
    match expected {
        Some(expected_values) => {
            assert_eq!(
                actual.len(),
                expected_values.len(),
                "optional slice lengths differ"
            );
            for (index, (actual_value, &expected_value)) in
                actual.iter().zip(expected_values).enumerate()
            {
                let actual_value =
                    actual_value.with_context(|| format!("value {index} was missing"));
                let actual_value = actual_value.expect("expected a blacklisted_fraction value");
                assert!(
                    (actual_value - expected_value).abs() <= 1e-9,
                    "value {index} differs: actual {actual_value}, expected {expected_value}"
                );
            }
        }
        None => assert!(
            actual.iter().all(Option::is_none),
            "expected all blacklisted_fraction values to be absent"
        ),
    }
}
