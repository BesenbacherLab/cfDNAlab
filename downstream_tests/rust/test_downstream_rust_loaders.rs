/// Check the sparse windowed end-motif downstream fixture.
fn check_sparse_windowed_end_motifs() -> Result<()> {
    let ends = load_ends_output(fixture_dir_path(
        "CFDNALAB_ENDS_SPARSE_WINDOWED_ZARR",
        "tiny_sparse_windowed.end_motifs.zarr",
        FixtureKind::Directory,
    )?)?;

    assert_eq!(ends.storage_mode(), EndMotifStorageMode::SparseCoo);
    assert_eq!(ends.row_mode(), EndMotifRowMode::BedWindows);
    assert_eq!(ends.motif_axis_kind(), EndMotifAxisKind::Motif);
    assert_eq!(ends.motif_labels(), &["_A", "_G"]);
    assert_eq!(ends.sparse_counts()?.nnz(), 3);
    assert_f64_slice_near(
        ends.sparse_counts()?.to_dense_matrix()?.values_row_major(),
        &[0.0, 1.0, 1.0, 0.0, 0.0, 1.0],
    );
    assert_f64_slice_near(
        ends.select()
            .windows(&[1])
            .read()?
            .to_dense_matrix()?
            .values_row_major(),
        &[1.0, 0.0],
    );
    assert_f64_slice_near(
        ends.select()
            .motifs_by_label(&["_G"])
            .read()?
            .to_dense_matrix()?
            .values_row_major(),
        &[1.0, 0.0, 1.0],
    );

    let windows = ends.window_metadata()?;
    assert_eq!(
        windows
            .iter()
            .map(|window| (
                window.index,
                window.chrom.as_str(),
                window.interval.as_tuple()
            ))
            .collect::<Vec<_>>(),
        vec![
            (0, "chr1", (10, 11)),
            (1, "chr1", (19, 20)),
            (2, "chr2", (10, 11))
        ]
    );
    assert!(
        windows
            .iter()
            .all(|window| window.blacklisted_fraction == Some(0.0))
    );

    let selected = ends
        .select()
        .windows(&[1, 0])
        .motifs_by_label(&["_G", "_A"])
        .read()?;
    assert_eq!(selected.shape(), (2, 2));
    assert_eq!(selected.row_indices(), &[1, 0]);
    assert_eq!(selected.motif_labels(), &["_G", "_A"]);
    assert_eq!(
        selected
            .window_metadata()?
            .iter()
            .map(|window| (
                window.index,
                window.chrom.as_str(),
                window.interval.as_tuple()
            ))
            .collect::<Vec<_>>(),
        vec![(1, "chr1", (19, 20)), (0, "chr1", (10, 11))]
    );
    assert_f64_slice_near(
        selected.to_dense_matrix()?.values_row_major(),
        &[0.0, 1.0, 1.0, 0.0],
    );

    Ok(())
}

