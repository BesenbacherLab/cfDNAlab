/// Check the sparse grouped end-motif downstream fixture.
fn check_sparse_grouped_end_motifs() -> Result<()> {
    let ends = load_ends_output(fixture_dir_path(
        "CFDNALAB_ENDS_SPARSE_GROUPED_ZARR",
        "tiny_sparse_grouped.end_motifs.zarr",
        FixtureKind::Directory,
    )?)?;

    assert_eq!(ends.storage_mode(), EndMotifStorageMode::SparseCoo);
    assert_eq!(ends.row_mode(), EndMotifRowMode::Groups);
    assert_eq!(ends.motif_axis_kind(), EndMotifAxisKind::Motif);
    assert_eq!(ends.motif_labels(), &["_A", "_G"]);
    assert_eq!(ends.group_index("alpha")?, 1);
    assert_f64_slice_near(
        ends.sparse_counts()?.to_dense_matrix()?.values_row_major(),
        &[1.0, 2.0, 1.0, 0.0, 0.0, 0.0],
    );
    assert_eq!(
        ends.group_metadata()?
            .iter()
            .map(|group| (
                group.index,
                group.name.as_str(),
                group.eligible_windows,
                group.blacklisted_fraction,
            ))
            .collect::<Vec<_>>(),
        vec![
            (0, "beta", 2, 0.0),
            (1, "alpha", 1, 0.0),
            (2, "gamma", 1, 0.0)
        ]
    );
    assert_f64_slice_near(
        ends.select()
            .groups_by_name(&["beta"])
            .read()?
            .to_dense_matrix()?
            .values_row_major(),
        &[1.0, 2.0],
    );
    assert_f64_slice_near(
        ends.select()
            .motifs_by_label(&["_A"])
            .read()?
            .to_dense_matrix()?
            .values_row_major(),
        &[1.0, 1.0, 0.0],
    );

    let selected = ends
        .select()
        .groups_by_name(&["alpha", "beta"])
        .motifs_by_label(&["_G", "_A"])
        .read()?;
    assert_eq!(selected.row_indices(), &[1, 0]);
    assert_eq!(
        selected
            .group_metadata()?
            .iter()
            .map(|group| group.name.as_str())
            .collect::<Vec<_>>(),
        vec!["alpha", "beta"]
    );
    assert_f64_slice_near(
        selected.to_dense_matrix()?.values_row_major(),
        &[0.0, 1.0, 2.0, 1.0],
    );
    let selected_dense_counts = selected.to_dense_matrix()?;
    assert_eq!(
        selected
            .group_metadata()?
            .iter()
            .zip(selected_dense_counts.rows())
            .map(|(group, motif_counts)| {
                (
                    group.name.as_str(),
                    motif_counts.iter().copied().sum::<f64>(),
                )
            })
            .collect::<Vec<_>>(),
        vec![("alpha", 1.0), ("beta", 3.0)]
    );
    let alpha = ends.select().groups_by_name(&["alpha"]).read()?;
    assert_f64_slice_near(alpha.to_dense_matrix()?.values_row_major(), &[1.0, 0.0]);
    let gamma = ends.select().groups_by_name(&["gamma"]).read()?;
    assert_f64_slice_near(gamma.to_dense_matrix()?.values_row_major(), &[0.0, 0.0]);

    Ok(())
}

